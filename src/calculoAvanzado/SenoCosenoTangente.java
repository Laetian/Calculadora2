package calculoAvanzado;

public class SenoCosenoTangente {
	public void seno(double input) {
		System.out.println("El seno de " + input + " es: " + Math.sin(input));

	}

	public void coseno(double input) {
		System.out.println("El coseno de " + input + " es: " + Math.cos(input));

	}

	public void tangente(double input) {
		System.out.println("La tangente de " + input + " es: " + Math.tan(input));

	}

}
