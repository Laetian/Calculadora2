package calculobasico;

public class MultiplicacionyDivision {
	public void multiplicacion(double a, double b) {
		System.out.println("Multiplicación de " + a + " por " + b + " da como resultado: " + (a * b));
	}

	public void division(double a, double b) {
		System.out.println("División de " + a + " entre " + b + " da como resultado: " + (a / b));
	}

}
